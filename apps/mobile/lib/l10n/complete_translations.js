// ── Apply translations to all locales ──────────────────────────────────
['ar', 'de', 'es', 'fr', 'he', 'pt'].forEach(loc => {
  const arbPath = path.join(dir, 'app_' + loc + '.arb');
  const arb = JSON.parse(fs.readFileSync(arbPath, 'utf8'));
  const locTrans = translations[loc];
  if (!locTrans) { console.log(loc + ': no translations found — using English fallback for missing keys'); return; }

  let added = 0;
  Object.keys(locTrans).forEach(k => {
    if (!(k in arb)) {
      arb[k] = locTrans[k];
      added++;
    }
  });

  // Write back with alphabetical key sorting
  const sorted = {};
  Object.keys(arb).sort().forEach(k => { sorted[k] = arb[k]; });
  fs.writeFileSync(arbPath, JSON.stringify(sorted, null, 2) + '\n');
  const count = Object.keys(sorted).filter(k => !k.startsWith('@')).length;
  console.log(loc + ': added ' + added + ' translations → ' + count + ' keys total');
});
