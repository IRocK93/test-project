locales = ["es", "fr", "de", "pt", "ar", "he", "zh", "it"]

en = json.load(open("app_en.arb", "r", encoding="utf-8"))
added = 0
for k, v in missing.items():
    if k not in en:
        en[k] = v
        added += 1
        en[f"@{k}"] = {"description": "Auto-generated localization key"}

with open("app_en.arb", "w", encoding="utf-8") as f:
    json.dump(en, f, indent=2, ensure_ascii=False)

print(f"Added {added} keys to app_en.arb")

for loc in locales:
    path = f"app_{loc}.arb"
    if not os.path.exists(path):
        print(f"{path} does not exist, skipping")
        continue
    data = json.load(open(path, "r", encoding="utf-8"))
    loc_added = 0
    for k, v in missing.items():
        if k not in data:
            data[k] = v
